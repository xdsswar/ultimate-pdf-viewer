public enum Fit {
    /**
     * Represents fitting vertically, i.e., adjusting the size to fit in the vertical direction.
     */
    VERTICAL,

    /**
     * Represents fitting horizontally, i.e., adjusting the size to fit in the horizontal direction.
     */
    HORIZONTAL,

    /**
     * Represents no fitting or none at all.
     */
    NONE

}
